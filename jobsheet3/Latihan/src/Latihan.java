public class Latihan {
    public static void main(String[] args) throws Exception {
        // increment
        int x = 1;
        System.out.println("x++ = " + x++);
        System.out.println("Setelah evaluasi, x = " + x);

        x = 1;
        System.out.println("++x = " + ++x);
        System.out.println("Setelah evaluasi, x = " + x);

        System.out.println(" ");

        System.out.println("x++ = " + x++);
        System.out.println("Setelah evaluasi, x = " + x);
        System.out.println("++x = " + ++x);
        System.out.println("Setelah evaluasi, x = " + x);

        System.out.println(" ");

        // decrement
        System.out.println("x-- = " + x--);
        System.out.println("Setelah evaluasi, x = " + x);

        System.out.println("++x = " + ++x);
        System.out.println("Setelah evaluasi, x = " + x);
    }
}
